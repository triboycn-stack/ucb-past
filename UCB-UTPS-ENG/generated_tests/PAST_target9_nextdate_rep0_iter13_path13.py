# 方法: PAST
# 目标文件: target9_nextdate.py
# 条件路径: month == 2
# 重复次数: 0, 迭代: 13
# 生成时间: 2026-04-26 07:00:22

import pytest
from target9_nextdate import next_date

@pytest.mark.parametrize("year, month, day, expected", [
    # 有效日期测试
    (2020, 2, 28, "2020-03-01"),  # 非闰年2月最后一天
    (2020, 2, 29, "2020-03-01"),  # 闰年2月最后一天
    (2021, 2, 28, "2021-03-01"),  # 非闰年2月最后一天
    (2024, 2, 29, "2024-03-01"),  # 闰年2月最后一天
    (2023, 2, 28, "2023-03-01"),  # 非闰年2月最后一天
    (2020, 12, 31, "2021-01-01"),  # 年末
    (2020, 11, 30, "2020-12-01"),  # 月末
    (2020, 1, 1, "2020-01-02"),   # 同月下一天
    (2020, 12, 15, "2020-12-16"),  # 同月下一天

    # 无效输入测试
    ("invalid", 2, 28, "INVALID_DATE"),
    (2020, "invalid", 28, "INVALID_DATE"),
    (2020, 2, "invalid", "INVALID_DATE"),
    (0, 2, 28, "INVALID_DATE"),
    (10000, 2, 28, "INVALID_DATE"),
    (2020, 0, 28, "INVALID_DATE"),
    (2020, 13, 28, "INVALID_DATE"),
    (2020, 2, 30, "INVALID_DATE"),
    (2021, 2, 30, "INVALID_DATE"),
    (2020, 4, 31, "INVALID_DATE"),

    # 边界条件测试
    (1, 1, 1, "0002-01-01"),
    (9999, 12, 31, "DATE_OUT_OF_RANGE"),
    (2020, 2, 1, "2020-02-02"),
    (2020, 2, 29, "2020-03-01"),
    (2020, 12, 31, "2021-01-01"),
    (2020, 1, 31, "2020-02-01"),
    (2020, 3, 31, "2020-04-01"),
    (2020, 4, 30, "2020-05-01"),
    (2020, 5, 31, "2020-06-01"),
    (2020, 6, 30, "2020-07-01"),
    (2020, 7, 31, "2020-08-01"),
    (2020, 8, 31, "2020-09-01"),
    (2020, 9, 30, "2020-10-01"),
    (2020, 10, 31, "2020-11-01"),
    (2020, 11, 30, "2020-12-01"),
    (2020, 12, 31, "2021-01-01"),
])
def test_next_date(year, month, day, expected):
    result = next_date(year, month, day)
    assert result == expected