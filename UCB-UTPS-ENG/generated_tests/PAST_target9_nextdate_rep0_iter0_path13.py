# 方法: PAST
# 目标文件: target9_nextdate.py
# 条件路径: month == 2
# 重复次数: 0, 迭代: 0
# 生成时间: 2026-04-18 16:12:11

import pytest
from target9_nextdate import next_date

@pytest.mark.parametrize("year, month, day, expected", [
    # 有效日期测试
    (2020, 2, 28, "2020-03-01"),  # 闰年2月最后一天
    (2021, 2, 28, "2021-03-01"),  # 非闰年2月最后一天
    (2020, 2, 29, "2020-03-01"),  # 闰年2月最后一天
    (2021, 2, 28, "2021-03-01"),  # 非闰年2月最后一天
    (2020, 12, 31, "2021-01-01"),  # 年末
    (2020, 1, 1, "2020-01-02"),  # 月初
    (2020, 4, 30, "2020-05-01"),  # 月末
    (2020, 6, 15, "2020-06-16"),  # 中间日期

    # 无效日期测试
    (0, 1, 1, "INVALID_DATE"),  # 年份小于1
    (10000, 1, 1, "INVALID_DATE"),  # 年份大于9999
    (2020, 0, 1, "INVALID_DATE"),  # 月份小于1
    (2020, 13, 1, "INVALID_DATE"),  # 月份大于12
    (2020, 2, 30, "INVALID_DATE"),  # 2月没有30天
    (2021, 2, 30, "INVALID_DATE"),  # 2月没有30天
    (2020, 4, 31, "INVALID_DATE"),  # 4月没有31天
    (2020, 6, 31, "INVALID_DATE"),  # 6月没有31天

    # 边界条件测试
    (1, 1, 1, "0001-01-02"),  # 最小年份
    (9999, 12, 31, "0001-01-01"),  # 最大年份+年末
    (2020, 2, 1, "2020-02-02"),  # 2月第一天
    (2020, 2, 28, "2020-03-01"),  # 闰年2月最后一天
    (2021, 2, 28, "2021-03-01"),  # 非闰年2月最后一天

    # 异常输入测试
    ("invalid", 1, 1, "INVALID_DATE"),  # 年份不是整数
    (2020, "invalid", 1, "INVALID_DATE"),  # 月份不是整数
    (2020, 1, "invalid", "INVALID_DATE"),  # 日不是整数
])
def test_next_date(year, month, day, expected):
    result = next_date(year, month, day)
    assert result == expected