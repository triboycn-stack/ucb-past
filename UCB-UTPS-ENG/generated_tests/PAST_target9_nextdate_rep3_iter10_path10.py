# 方法: PAST
# 目标文件: target9_nextdate.py
# 条件路径: day < max_day
# 重复次数: 3, 迭代: 10
# 生成时间: 2026-04-26 07:07:48

import pytest
from target9_nextdate import next_date

@pytest.mark.parametrize("year, month, day, expected", [
    # 有效日期，非月末
    (2023, 10, 5, "2023-10-06"),
    # 有效日期，月末（非年末）
    (2023, 10, 31, "2023-11-01"),
    # 有效日期，年末
    (2023, 12, 31, "2024-01-01"),
    # 闰年二月最后一天
    (2020, 2, 29, "2020-03-01"),
    # 非闰年二月最后一天
    (2021, 2, 28, "2021-03-01"),
    # 月份天数边界测试（4月30日）
    (2023, 4, 30, "2023-05-01"),
    # 月份天数边界测试（6月30日）
    (2023, 6, 30, "2023-07-01"),
    # 月份天数边界测试（9月30日）
    (2023, 9, 30, "2023-10-01"),
    # 月份天数边界测试（11月30日）
    (2023, 11, 30, "2023-12-01"),
    # 年份边界测试（9999年12月31日）
    (9999, 12, 31, "DATE_OUT_OF_RANGE"),
    # 输入验证：非整数
    ("2023", 10, 5, "INVALID_DATE"),
    (2023, "10", 5, "INVALID_DATE"),
    (2023, 10, "5", "INVALID_DATE"),
    # 输入验证：年份越界
    (0, 10, 5, "INVALID_DATE"),
    (10000, 10, 5, "INVALID_DATE"),
    # 输入验证：月份越界
    (2023, 0, 5, "INVALID_DATE"),
    (2023, 13, 5, "INVALID_DATE"),
    # 输入验证：日期越界（非二月）
    (2023, 2, 30, "INVALID_DATE"),
    # 输入验证：日期越界（二月）
    (2023, 2, 29, "INVALID_DATE"),
    # 输入验证：无效日期（非闰年二月29日）
    (2023, 2, 29, "INVALID_DATE"),
    # 输入验证：无效日期（四月31日）
    (2023, 4, 31, "INVALID_DATE"),
    # 输入验证：无效日期（六月31日）
    (2023, 6, 31, "INVALID_DATE"),
    # 输入验证：无效日期（九月31日）
    (2023, 9, 31, "INVALID_DATE"),
    # 输入验证：无效日期（十一月31日）
    (2023, 11, 31, "INVALID_DATE"),
])
def test_next_date(year, month, day, expected):
    result = next_date(year, month, day)
    assert result == expected