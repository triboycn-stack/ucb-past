import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 输入验证测试
    ("  hello\n    world", 10, 5, -1.0),
    (10, "  hello\n    world", 5, -1.0),
    (10, 10, "  hello\n    world", -1.0),
    (10, 10, -5, -1.0),
    (-5, 10, 10, -1.0),
    (10, -5, 10, -1.0),
    (10.5, 10, 10, -1.0),
    (10, 10.5, 10, -1.0),
    (10, 10, 10.5, -1.0),
    # 销售额 ≤ 1000 的情况
    (0, 0, 0, 0.00),
    (1, 0, 0, 4.50),
    (0, 2, 0, 6.00),
    (0, 0, 4, 5.00),
    (2, 1, 1, 13.50),
    # 销售额 ≤ 1800 的情况
    (10, 10, 10, 100.00 + (450 + 300 + 250 - 1000) * 0.15),
    (15, 10, 10, 100.00 + (675 + 300 + 250 - 1000) * 0.15),
    (10, 15, 10, 100.00 + (450 + 450 + 250 - 1000) * 0.15),
    (10, 10, 15, 100.00 + (450 + 300 + 375 - 1000) * 0.15),
    # 销售额 > 1800 的情况
    (20, 20, 20, 220.00 + (900 + 600 + 500 - 1800) * 0.20),
    (30, 30, 30, 220.00 + (1350 + 900 + 750 - 1800) * 0.20),
    (10, 20, 30, 220.00 + (450 + 600 + 750 - 1800) * 0.20),
    # 边界值测试
    (0, 0, 0, 0.00),
    (22, 0, 0, 99.00),  # 22*45 = 990 → 990*0.10 = 99.00
    (23, 0, 0, 100.00 + (1035 - 1000)*0.15),  # 23*45=1035 → 100 + 35*0.15 = 105.25
    (40, 0, 0, 220.00 + (1800 - 1800)*0.20),  # 40*45=1800 → 220.00
    (41, 0, 0, 220.00 + (1845 - 1800)*0.20),  # 41*45=1845 → 220 + 45*0.20 = 229.00
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert result == expected