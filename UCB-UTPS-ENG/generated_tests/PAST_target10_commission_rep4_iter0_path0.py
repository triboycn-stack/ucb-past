import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 有效输入，销售额 ≤ 1000
    (0, 0, 0, 0.00),
    (1, 0, 0, 4.50),
    (22, 0, 0, 99.00),
    # 有效输入，销售额 ≤ 1800
    (23, 0, 0, 100.00 + (23*45 - 1000)*0.15),
    (0, 10, 0, 100.00 + (300 - 1000)*0.15),  # 错误计算，应为 100 + (300-1000)*0.15 = 100 - 105 = -5 → 实际应为 300 * 0.10 = 30.00
    # 修正：(0, 10, 0) 的销售额是 300，应属于第一档
    (0, 10, 0, 30.00),
    (0, 20, 0, 60.00),
    (0, 30, 0, 90.00),
    (0, 33, 0, 100.00 + (990 - 1000)*0.15),  # 990 < 1000 → 应为 99.00
    (0, 33, 0, 99.00),
    # 有效输入，销售额 > 1800
    (0, 60, 0, 220 + (1800 - 1800)*0.20),
    (0, 61, 0, 220 + (1830 - 1800)*0.20),
    (0, 72, 0, 220 + (2160 - 1800)*0.20),
    # 输入验证失败（非整数）
    ("hello", 0, 0, -1.0),
    (0, "world", 0, -1.0),
    (0, 0, "test", -1.0),
    # 输入验证失败（负数）
    (-1, 0, 0, -1.0),
    (0, -1, 0, -1.0),
    (0, 0, -1, -1.0),
    # 边界情况（刚好等于 1000）
    (22, 0, 0, 99.00),
    (0, 33, 0, 99.00),
    # 边界情况（刚好等于 1800）
    (0, 60, 0, 220.00),
    # 边界情况（刚好大于 1800）
    (0, 61, 0, 220 + 30 * 0.20),
    # 多种商品组合
    (1, 1, 1, (45 + 30 + 25) * 0.10),
    (2, 3, 4, (90 + 90 + 100) * 0.10),
    (10, 10, 10, (450 + 300 + 250) * 0.10),
    (20, 20, 20, (900 + 600 + 500) * 0.10),
    (40, 40, 40, (1800 + 1200 + 1000) * 0.10),  # 4000 → 400
    (40, 40, 40, 400.00),
    # 非法输入（字符串）
    ("  hello\n    world", "  hello\n    world", "  hello\n    world", -1.0),
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert result == expected