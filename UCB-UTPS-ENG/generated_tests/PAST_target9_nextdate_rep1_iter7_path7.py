# 方法: PAST
# 目标文件: target9_nextdate.py
# 条件路径: year < 1 or year > 9999
# 重复次数: 1, 迭代: 7
# 生成时间: 2026-04-26 07:02:01

import pytest
from target9_nextdate import next_date

@pytest.mark.parametrize("year, month, day, expected", [
    # 有效日期测试
    (2023, 1, 1, "2023-01-02"),
    (2023, 12, 31, "2024-01-01"),
    (2020, 2, 28, "2020-03-01"),
    (2021, 2, 28, "2021-03-01"),
    (2020, 12, 31, "2021-01-01"),
    (2021, 1, 31, "2021-02-01"),
    (2021, 3, 31, "2021-04-01"),
    (2021, 4, 30, "2021-05-01"),
    (2021, 5, 31, "2021-06-01"),
    (2021, 6, 30, "2021-07-01"),
    (2021, 7, 31, "2021-08-01"),
    (2021, 8, 31, "2021-09-01"),
    (2021, 9, 30, "2021-10-01"),
    (2021, 10, 31, "2021-11-01"),
    (2021, 11, 30, "2021-12-01"),
    (2021, 12, 31, "2022-01-01"),
    
    # 无效输入测试
    ("test", 1, 1, "INVALID_DATE"),
    (1, "test", 1, "INVALID_DATE"),
    (1, 1, "test", "INVALID_DATE"),
    (0, 1, 1, "INVALID_DATE"),
    (10000, 1, 1, "INVALID_DATE"),
    (1, 0, 1, "INVALID_DATE"),
    (1, 13, 1, "INVALID_DATE"),
    (1, 2, 30, "INVALID_DATE"),
    (1, 2, 31, "INVALID_DATE"),
    (1, 4, 31, "INVALID_DATE"),
    (1, 6, 31, "INVALID_DATE"),
    (1, 9, 31, "INVALID_DATE"),
    (1, 11, 31, "INVALID_DATE"),
    
    # 边界条件测试
    (1, 1, 1, "0001-01-02"),
    (9999, 12, 31, "DATE_OUT_OF_RANGE"),
    (1, 1, 31, "INVALID_DATE"),
    (1, 12, 31, "0002-01-01"),
    (2020, 2, 29, "2020-03-01"),
    (2021, 2, 28, "2021-03-01"),
    (2020, 12, 31, "2021-01-01"),
    (2021, 12, 31, "2022-01-01"),
    
    # 条件分支覆盖测试
    (1, 1, 1, "0001-01-02"),  # year < 1
    (10000, 1, 1, "INVALID_DATE"),  # year > 9999
    (2023, 2, 28, "2023-03-01"),  # month == 2, not leap year
    (2020, 2, 28, "2020-03-01"),  # month == 2, leap year
    (2023, 12, 31, "2024-01-01"),  # month == 12
    (2023, 1, 31, "2023-02-01"),  # month != 12, day == max_day
])
def test_next_date(year, month, day, expected):
    result = next_date(year, month, day)
    assert result == expected