import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 有效输入，销售额 <= 1000
    (0, 0, 0, 0.00),
    (1, 0, 0, 4.50),
    (2, 0, 0, 9.00),
    (0, 3, 0, 9.00),
    (0, 0, 4, 10.00),
    (1, 1, 1, 10.00),
    # 有效输入，销售额在 1000-1800 之间
    (10, 10, 10, 100.00 + (450 - 1000) * 0.15),  # 销售额 = 10*45 + 10*30 + 10*25 = 1000
    (11, 10, 10, 100.00 + (495 - 1000) * 0.15),  # 销售额 = 11*45 + 10*30 + 10*25 = 1095
    (15, 15, 15, 100.00 + (15*45 + 15*30 + 15*25 - 1000) * 0.15),  # 销售额 = 15*45 + 15*30 + 15*25 = 1575
    # 有效输入，销售额 > 1800
    (20, 20, 20, 220.00 + (20*45 + 20*30 + 20*25 - 1800) * 0.20),  # 销售额 = 20*45 + 20*30 + 20*25 = 2000
    (25, 25, 25, 220.00 + (25*45 + 25*30 + 25*25 - 1800) * 0.20),  # 销售额 = 25*45 + 25*30 + 25*25 = 2675
    # 无效输入（类型错误）
    ("hello", 0, 0, -1.0),
    (0, "world", 0, -1.0),
    (0, 0, "test", -1.0),
    (0.5, 0, 0, -1.0),
    (0, 0.5, 0, -1.0),
    (0, 0, 0.5, -1.0),
    # 无效输入（负数）
    (-1, 0, 0, -1.0),
    (0, -1, 0, -1.0),
    (0, 0, -1, -1.0),
    # 边界情况（销售额刚好为 1000）
    (22, 0, 0, 100.00),  # 22*45 = 990 → 990 + 0 + 0 = 990 → 不够 1000？需要重新计算
    (23, 0, 0, 100.00),  # 23*45 = 1035 → 1035 > 1000 → 应该进入第二个分支
    # 边界情况（销售额刚好为 1800）
    (40, 0, 0, 220.00),  # 40*45 = 1800 → 进入 else 分支
    # 空值（所有参数为 0）
    (0, 0, 0, 0.00),
    # 非整数字符串（但不是数字）
    ("  hello\n    world", "  hello\n    world", "  hello\n    world", -1.0),
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert result == expected