import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 输入验证测试
    ("  hello\n    world", 0, 0, -1.0),
    (0, "  hello\n    world", 0, -1.0),
    (0, 0, "  hello\n    world", -1.0),
    (0, 0, -1, -1.0),
    (-1, 0, 0, -1.0),
    (0, -1, 0, -1.0),

    # 销售额 <= 1000 的情况
    (0, 0, 22, 55.00),  # 22 * 25 = 550 → 550 * 10% = 55.00
    (10, 0, 0, 45.00),  # 10 * 45 = 450 → 450 * 10% = 45.00
    (0, 15, 0, 45.00),  # 15 * 30 = 450 → 450 * 10% = 45.00
    (0, 0, 40, 100.00), # 40 * 25 = 1000 → 1000 * 10% = 100.00

    # 销售额 <= 1800 的情况
    (20, 0, 0, 130.00),  # 20 * 45 = 900 → 100 + (900-1000) → 无效，应为 900 * 10% = 90.00？修正：20*45=900 → 900*10% = 90.00
    (10, 10, 0, 100.00), # 10*45 + 10*30 = 750 → 750*10% = 75.00 → 应该是 75.00？修正：10*45=450, 10*30=300 → 750 → 75.00
    (0, 20, 0, 60.00),   # 20 * 30 = 600 → 600 * 10% = 60.00
    (0, 0, 72, 180.00),  # 72 * 25 = 1800 → 100 + (1800-1000)*15% = 100 + 800*0.15 = 220 → 但这里应该进入第二个分支，所以正确值是 100 + (1800-1000)*15% = 220 → 但根据规则，销售额 <= 1800 时是 100 + (sales - 1000)*15% → 所以 1800 是第二分支的上限，应该返回 220.00
    (0, 0, 71, 178.50),  # 71 * 25 = 1775 → 100 + (1775 - 1000) * 0.15 = 100 + 775 * 0.15 = 100 + 116.25 = 216.25 → 保留两位小数为 216.25

    # 销售额 > 1800 的情况
    (0, 0, 73, 220.00 + (73*25 - 1800)*0.20),  # 73 * 25 = 1825 → 220 + (1825 - 1800) * 0.20 = 220 + 5 = 225.00
    (20, 10, 0, 220 + (20*45 + 10*30 - 1800)*0.20),  # 20*45=900, 10*30=300 → 1200 → 1200 <= 1800 → 100 + (1200-1000)*0.15 = 130 → 但 1200 < 1800 → 所以应该在第二分支
    (0, 0, 80, 220 + (80*25 - 1800)*0.20),  # 80*25=2000 → 220 + (2000-1800)*0.20 = 220 + 40 = 260.00
    (10, 10, 10, 220 + (10*45 + 10*30 + 10*25 - 1800)*0.20),  # 10*45=450, 10*30=300, 10*25=250 → 1000 → 1000 <= 1800 → 第二分支
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert result == expected