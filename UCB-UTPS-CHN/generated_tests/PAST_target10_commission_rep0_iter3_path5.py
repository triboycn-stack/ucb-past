import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 输入验证测试
    ("  hello\n    world", 10, 5, -1.0),
    (10, "  hello\n    world", 5, -1.0),
    (10, 10, "  hello\n    world", -1.0),
    (-1, 10, 5, -1.0),
    (10, -1, 5, -1.0),
    (10, 10, -1, -1.0),
    # 销售额 ≤ 1000
    (0, 0, 0, 0.0),
    (1, 0, 0, 4.5),
    (0, 2, 0, 6.0),
    (0, 0, 4, 5.0),
    (2, 1, 1, 13.5),
    # 销售额 ≤ 1800
    (10, 10, 10, 100.0 + (450 + 300 + 250 - 1000) * 0.15),
    (15, 10, 10, 100.0 + (675 + 300 + 250 - 1000) * 0.15),
    # 销售额 > 1800
    (20, 20, 20, 220.0 + (900 + 600 + 500 - 1800) * 0.20),
    (30, 30, 30, 220.0 + (1350 + 900 + 750 - 1800) * 0.20),
    # 边界值测试
    (0, 0, 0, 0.0),
    (22, 0, 0, 99.0),  # 22*45=990 → 990*0.1=99.0
    (23, 0, 0, 100.0 + (1035 - 1000)*0.15),  # 23*45=1035 → 100 + 35*0.15=105.25
    (40, 0, 0, 220.0 + (1800 - 1800)*0.20),  # 40*45=1800 → 220.0
    (41, 0, 0, 220.0 + (1845 - 1800)*0.20),  # 41*45=1845 → 220 + 45*0.2=229.0
    # 多种商品组合
    (10, 10, 10, 100.0 + (450 + 300 + 250 - 1000) * 0.15),
    (5, 5, 5, 100.0 + (225 + 150 + 125 - 1000) * 0.15),  # 500 → 100 + (500-1000) → 不成立，应为 500*0.1=50.0
    (10, 5, 5, 100.0 + (450 + 150 + 125 - 1000) * 0.15),  # 725 → 72.5
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert result == expected