import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 输入验证测试
    ("invalid", 10, 20, -1.0),
    (10, "invalid", 20, -1.0),
    (10, 20, "invalid", -1.0),
    (-1, 10, 20, -1.0),
    (10, -1, 20, -1.0),
    (10, 20, -1, -1.0),
    # 销售额 ≤ 1000 的情况
    (0, 0, 0, 0.00),
    (1, 0, 0, 4.50),
    (0, 3, 0, 9.00),
    (0, 0, 40, 100.00),
    # 销售额 ≤ 1800 的情况
    (10, 10, 10, 100.00 + (450 + 300 + 250 - 1000) * 0.15),
    (20, 0, 0, 100.00 + (900 - 1000) * 0.15),  # 900 < 1000，应为 900 * 0.10 = 90.00
    # 销售额 > 1800 的情况
    (40, 0, 0, 220 + (1800 - 1800) * 0.20),
    (50, 0, 0, 220 + (2250 - 1800) * 0.20),
    # 边界值测试
    (0, 0, 40, 100.00),
    (0, 0, 41, 100.00 + 25 * 0.15),
    (0, 0, 72, 220 + (1800 - 1800) * 0.20),
    (0, 0, 73, 220 + 25 * 0.20),
    # 复杂组合测试
    (10, 10, 10, 100.00 + (450 + 300 + 250 - 1000) * 0.15),
    (20, 20, 20, 220 + (900 + 600 + 500 - 1800) * 0.20),
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert result == expected