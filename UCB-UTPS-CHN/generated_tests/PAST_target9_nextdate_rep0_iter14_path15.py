    (2020, "1", 1, "INVALID_DATE"),
    (2020, 1, "1", "INVALID_DATE"),
    (2020, 1.5, 1, "INVALID_DATE"),
    (2020, 1, 1.5, "INVALID_DATE"),
    
    # 年份边界测试
    (9999, 12, 31, "0001-01-01"),
    (9999, 1, 1, "10000-01-01"),
    (9998, 12, 31, "10000-01-01"),
    
    # 特殊情况测试
    (2020, 2, 29, "2020-03-01"),
