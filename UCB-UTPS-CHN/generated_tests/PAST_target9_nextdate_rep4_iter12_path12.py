# 方法: PAST
# 目标文件: target9_nextdate.py
# 条件路径: month < 12
# 重复次数: 4, 迭代: 12
# 生成时间: 2026-04-26 07:10:49

import pytest
from target9_nextdate import next_date

@pytest.mark.parametrize("year, month, day, expected", [
    # 有效日期，非月末
    (2023, 4, 5, "2023-04-06"),
    # 有效日期，月末（非年末）
    (2023, 4, 30, "2023-05-01"),
    # 有效日期，年末
    (2023, 12, 31, "2024-01-01"),
    # 无效年份
    (0, 1, 1, "INVALID_DATE"),
    (10000, 1, 1, "INVALID_DATE"),
    # 无效月份
    (2023, 0, 1, "INVALID_DATE"),
    (2023, 13, 1, "INVALID_DATE"),
    # 无效日期（非闰年二月）
    (2023, 2, 29, "INVALID_DATE"),
    # 有效日期（闰年二月）
    (2020, 2, 29, "2020-03-01"),
    # 无效日期（非该月最大天数）
    (2023, 4, 31, "INVALID_DATE"),
    # 年份超出范围
    (9999, 12, 31, "2000-01-01"),
    # 输入类型错误
    ("2023", 4, 5, "INVALID_DATE"),
    (2023, "4", 5, "INVALID_DATE"),
    (2023, 4, "5", "INVALID_DATE"),
    # 边界条件：最小年份
    (1, 1, 1, "0001-01-02"),
    # 边界条件：最大年份
    (9999, 12, 31, "0001-01-01"),
    # 边界条件：最小月份
    (2023, 1, 1, "2023-02-01"),
    # 边界条件：最大月份
    (2023, 12, 31, "2024-01-01"),
    # 边界条件：最小日期
    (2023, 1, 1, "2023-02-01"),
    # 边界条件：最大日期（非闰年二月）
    (2023, 2, 28, "2023-03-01"),
    # 边界条件：最大日期（闰年二月）
    (2020, 2, 29, "2020-03-01"),
])
def test_next_date(year, month, day, expected):
    result = next_date(year, month, day)
    assert result == expected