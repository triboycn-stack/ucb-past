    (-1, 0, 0, -1.0),
    (0, -1, 0, -1.0),
    (0, 0, -1, -1.0),
    # 销售额为负数（应返回 -1.0）
    (1, 1, 100, -1.0),
    # 浮点数输入（应返回 -1.0）
    (1.5, 2, 3, -1.0),
    (2, 3.5, 4, -1.0),
    (5, 6, 7.5, -1.0),
