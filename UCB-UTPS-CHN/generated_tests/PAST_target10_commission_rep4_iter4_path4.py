    ("  hello\n    world", 0, 0, -1.0),
    (0, "  hello\n    world", 0, -1.0),
    (0, 0, "  hello\n    world", -1.0),
    (0, 0, -1, -1.0),
    (-1, 0, 0, -1.0),
    (0, -1, 0, -1.0),
    (0, 0, -2, -1.0),

    # 销售额 <= 1000 的情况
    (0, 0, 0, 0.0),
    (1, 0, 0, 4.5),
    (0, 3, 0, 9.0),
    (0, 0, 4, 10.0),
    (2, 1, 1, 16.5),

    # 销售额 <= 1800 的情况
