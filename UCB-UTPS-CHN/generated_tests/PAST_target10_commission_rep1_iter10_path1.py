import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 输入验证测试
    ("  hello\n    world", 10, 5, -1.0),
    (10, "  hello\n    world", 5, -1.0),
    (10, 10, "  hello\n    world", -1.0),
    (10, 10, 5, -1.0),  # 正常输入，但后续测试用例会覆盖

    # 销售额 ≤ 1000 的情况
    (0, 0, 0, 0.00),
    (1, 0, 0, 4.50),
    (0, 3, 0, 9.00),
    (0, 0, 40, 100.00),

    # 销售额 ≤ 1800 的情况
    (1, 1, 1, 10.00 + 1.50 + 1.50 + 1.50),  # 45+30+25=100 → 100*0.10 = 10.00
    (2, 2, 2, 20.00 + 3.00 + 3.00 + 3.00),  # 90+60+50=200 → 100 + 100*0.15 = 115.00
    (10, 0, 0, 100.00 + 0.00),  # 450 → 100 + 350*0.15 = 152.50
    (0, 10, 0, 100.00 + 200*0.15),  # 300 → 100 + 200*0.15 = 130.00
    (0, 0, 72, 100.00 + 200*0.15),  # 1800 → 100 + 800*0.15 = 220.00

    # 销售额 > 1800 的情况
    (10, 10, 10, 220.00 + 100*0.20),  # 450+300+250=1000 → 220 + 800*0.20 = 380.00
    (20, 20, 20, 220.00 + 200*0.20),  # 900+600+500=2000 → 220 + 200*0.20 = 260.00
    (0, 0, 100, 220.00 + 200*0.20),  # 2500 → 220 + 700*0.20 = 360.00

    # 边界条件测试
    (0, 0, 0, 0.00),
    (0, 0, 1, 25.00 * 0.10),  # 25 → 2.50
    (0, 0, 40, 100.00),
    (0, 0, 72, 220.00),
    (0, 0, 73, 220.00 + 25.00 * 0.20),  # 1825 → 220 + 25*0.20 = 225.00

    # 非法输入测试
    (-1, 0, 0, -1.00),
    (0, -1, 0, -1.00),
    (0, 0, -1, -1.00),
    ("abc", 0, 0, -1.00),
    (0, "abc", 0, -1.00),
    (0, 0, "abc", -1.00),
    (1.5, 0, 0, -1.00),
    (0, 1.5, 0, -1.00),
    (0, 0, 1.5, -1.00),
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert round(result, 2) == round(expected, 2)