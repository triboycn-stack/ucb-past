# 方法: PAST
# 目标文件: target9_nextdate.py
# 条件路径: month == 2
# 重复次数: 1, 迭代: 7
# 生成时间: 2026-04-18 16:17:40

import pytest
from target9_nextdate import next_date

@pytest.mark.parametrize("year, month, day, expected", [
    # 有效日期测试
    (2020, 2, 28, "2020-03-01"),  # 非闰年二月最后一天
    (2020, 2, 29, "2020-03-01"),  # 闰年二月最后一天
    (2021, 2, 28, "2021-03-01"),  # 非闰年二月最后一天
    (2024, 2, 29, "2024-03-01"),  # 闰年二月最后一天
    (2023, 2, 28, "2023-03-01"),  # 非闰年二月最后一天
    (2020, 12, 31, "2021-01-01"),  # 年末
    (2020, 11, 30, "2020-12-01"),  # 月末
    (2020, 1, 1, "2020-01-02"),  # 月初
    (2020, 4, 30, "2020-05-01"),  # 月末
    (2020, 6, 30, "2020-07-01"),  # 月末
    (2020, 9, 30, "2020-10-01"),  # 月末
    (2020, 11, 30, "2020-12-01"),  # 月末

    # 无效日期测试
    (0, 1, 1, "INVALID_DATE"),  # 年份小于1
    (10000, 1, 1, "INVALID_DATE"),  # 年份大于9999
    (2020, 0, 1, "INVALID_DATE"),  # 月份小于1
    (2020, 13, 1, "INVALID_DATE"),  # 月份大于12
    (2020, 2, 30, "INVALID_DATE"),  # 二月超过天数
    (2021, 2, 29, "INVALID_DATE"),  # 非闰年二月30日
    (2020, 4, 31, "INVALID_DATE"),  # 四月31日
    (2020, 6, 31, "INVALID_DATE"),  # 六月31日
    (2020, 9, 31, "INVALID_DATE"),  # 九月31日
    (2020, 11, 31, "INVALID_DATE"),  # 十一月31日
    (2020, 1, 0, "INVALID_DATE"),  # 日小于1
    (2020, 1, 32, "INVALID_DATE"),  # 日大于最大天数

    # 参数类型错误
    ("2020", 2, 28, "INVALID_DATE"),  # 年份不是整数
    (2020, "2", 28, "INVALID_DATE"),  # 月份不是整数
    (2020, 2, "28", "INVALID_DATE"),  # 日不是整数

    # 年份边界测试
    (9999, 12, 31, "0000-01-01"),  # 年份最大值，下一年超出范围
])
def test_next_date(year, month, day, expected):
    result = next_date(year, month, day)
    assert result == expected