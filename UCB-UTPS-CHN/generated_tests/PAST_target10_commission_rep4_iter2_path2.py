import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 输入验证测试
    ("  hello\n    world", 10, 5, -1.0),
    (10, "  hello\n    world", 5, -1.0),
    (10, 10, "  hello\n    world", -1.0),
    (10, 10, -5, -1.0),
    (-5, 10, 10, -1.0),
    (10, -5, 10, -1.0),
    (10, 10, -5, -1.0),
    (10, 10, 0, 10*45 + 10*30 + 0*25),
    # 销售额 ≤ 1000 测试
    (0, 0, 0, 0.0),
    (1, 0, 0, 4.5),
    (0, 3, 0, 9.0),
    (0, 0, 40, 100.0),
    # 销售额 ≤ 1800 测试
    (1, 1, 1, 100.0 + (45+30+25-1000)*0.15),
    (2, 2, 2, 100.0 + (2*45 + 2*30 + 2*25 - 1000)*0.15),
    (0, 0, 40, 100.0),
    (0, 0, 41, 100.0 + (41*25 - 1000)*0.15),
    # 销售额 > 1800 测试
    (0, 0, 72, 220.0 + (72*25 - 1800)*0.20),
    (1, 1, 72, 220.0 + (45+30+72*25 - 1800)*0.20),
    (2, 3, 72, 220.0 + (2*45 + 3*30 + 72*25 - 1800)*0.20),
    # 边界值测试
    (0, 0, 40, 100.0),
    (0, 0, 41, 100.0 + (41*25 - 1000)*0.15),
    (0, 0, 72, 220.0 + (72*25 - 1800)*0.20),
    (0, 0, 73, 220.0 + (73*25 - 1800)*0.20),
    # 多种组合测试
    (10, 10, 10, 10*45 + 10*30 + 10*25),
    (5, 5, 5, 5*45 + 5*30 + 5*25),
    (0, 0, 0, 0.0),
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert result == expected