import pytest
from target10_commission import calculate_commission

@pytest.mark.parametrize("locks, stocks, barrels, expected", [
    # 输入验证测试
    ("  hello\n    world", 10, 5, -1.0),
    (10, "  hello\n    world", 5, -1.0),
    (10, 10, "  hello\n    world", -1.0),
    (-1, 10, 5, -1.0),
    (10, -1, 5, -1.0),
    (10, 10, -1, -1.0),
    # 销售额 <= 1000
    (0, 0, 0, 0.00),
    (1, 0, 0, 4.50),
    (0, 3, 0, 9.00),
    (0, 0, 40, 100.00),
    # 销售额 > 1000 且 <= 1800
    (1, 1, 20, 100.00 + (1*45 + 1*30 + 20*25 - 1000) * 0.15),
    (2, 2, 20, 100.00 + (2*45 + 2*30 + 20*25 - 1000) * 0.15),
    # 销售额 > 1800
    (10, 10, 10, 220.00 + (10*45 + 10*30 + 10*25 - 1800) * 0.20),
    (20, 20, 20, 220.00 + (20*45 + 20*30 + 20*25 - 1800) * 0.20),
    # 边界值测试
    (0, 0, 40, 100.00),
    (0, 0, 41, 100.00 + (41*25) - 1000 * 0.15),
    (0, 0, 72, 220.00 + (72*25 - 1800) * 0.20),
    # 非整数输入（应返回-1.0）
    (1.5, 10, 5, -1.0),
    (10, 1.5, 5, -1.0),
    (10, 10, 1.5, -1.0),
    # 大数值测试
    (1000, 1000, 1000, 220.00 + (1000*45 + 1000*30 + 1000*25 - 1800) * 0.20),
])
def test_calculate_commission(locks, stocks, barrels, expected):
    result = calculate_commission(locks, stocks, barrels)
    assert result == expected