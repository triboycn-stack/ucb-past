# 方法: PAST
# 目标文件: target9_nextdate.py
# 条件路径: not (day < 1 or day > max_day)
# 重复次数: 4, 迭代: 3
# 生成时间: 2026-04-26 07:09:18

import pytest
from target9_nextdate import next_date

@pytest.mark.parametrize("year, month, day, expected", [
    # 有效日期测试
    (2020, 2, 28, "2020-03-01"),  # 平年2月最后一天
    (2020, 2, 29, "2020-03-01"),  # 闰年2月最后一天
    (2021, 12, 31, "2022-01-01"),  # 年末
    (2021, 1, 1, "2021-01-02"),  # 月初
    (2021, 4, 30, "2021-05-01"),  # 月末
    (2021, 6, 15, "2021-06-16"),  # 中间日期

    # 无效日期测试
    (0, 1, 1, "INVALID_DATE"),  # 年份小于1
    (10000, 1, 1, "INVALID_DATE"),  # 年份大于9999
    (2020, 0, 1, "INVALID_DATE"),  # 月份小于1
    (2020, 13, 1, "INVALID_DATE"),  # 月份大于12
    (2020, 2, 30, "INVALID_DATE"),  # 2月不存在30日
    (2021, 4, 31, "INVALID_DATE"),  # 4月不存在31日
    (2021, 2, 0, "INVALID_DATE"),  # 日小于1
    (2021, 2, 31, "INVALID_DATE"),  # 日大于最大天数

    # 参数类型错误
    ("2020", 2, 28, "INVALID_DATE"),  # 年份不是整数
    (2020, "2", 28, "INVALID_DATE"),  # 月份不是整数
    (2020, 2, "28", "INVALID_DATE"),  # 日不是整数

    # 边界条件测试
    (1, 1, 1, "0001-01-02"),  # 最小年份、月份、日期
    (9999, 12, 31, "DATE_OUT_OF_RANGE"),  # 年份最大值，下一年超出范围
    (2020, 12, 31, "2021-01-01"),  # 年末，下一年
    (2020, 1, 31, "2020-02-01"),  # 1月最后一天
    (2020, 3, 31, "2020-04-01"),  # 3月最后一天
    (2020, 5, 31, "2020-06-01"),  # 5月最后一天
    (2020, 7, 31, "2020-08-01"),  # 7月最后一天
    (2020, 8, 31, "2020-09-01"),  # 8月最后一天
    (2020, 10, 31, "2020-11-01"),  # 10月最后一天
    (2020, 12, 31, "2021-01-01"),  # 12月最后一天

    # 条件分支覆盖
    (2020, 2, 28, "2020-03-01"),  # day < max_day
    (2020, 2, 29, "2020-03-01"),  # month < 12
    (2020, 12, 31, "2021-01-01"),  # else 分支
])
def test_next_date(year, month, day, expected):
    result = next_date(year, month, day)
    assert result == expected