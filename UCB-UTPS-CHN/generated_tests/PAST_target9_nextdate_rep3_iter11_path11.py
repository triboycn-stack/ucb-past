import pytest
from target9_nextdate import next_date

@pytest.mark.parametrize("year, month, day, expected", [
    # 有效日期测试
    (2023, 1, 1, "2023-01-02"),
    (2023, 1, 31, "2023-02-01"),
    (2020, 2, 28, "2020-03-01"),
    (2021, 2, 28, "2021-03-01"),
    (2023, 12, 31, "2024-01-01"),
    (2023, 6, 15, "2023-06-16"),
    (2023, 7, 31, "2023-08-01"),
    (2023, 4, 30, "2023-05-01"),
    (2023, 9, 30, "2023-10-01"),
    (2023, 11, 30, "2023-12-01"),
    
    # 无效输入测试
    ("test", 1, 1, "INVALID_DATE"),
    (1, "test", 1, "INVALID_DATE"),
    (1, 1, "test", "INVALID_DATE"),
    (0, 1, 1, "INVALID_DATE"),
    (10000, 1, 1, "INVALID_DATE"),
    (1, 0, 1, "INVALID_DATE"),
    (1, 13, 1, "INVALID_DATE"),
    (1, 2, 30, "INVALID_DATE"),
    (1, 2, 31, "INVALID_DATE"),
    (1, 4, 31, "INVALID_DATE"),
    (1, 6, 31, "INVALID_DATE"),
    (1, 9, 31, "INVALID_DATE"),
    (1, 11, 31, "INVALID_DATE"),
    
    # 边界条件测试
    (1, 1, 1, "0001-01-02"),
    (9999, 12, 31, "DATE_OUT_OF_RANGE"),
    (2023, 1, 1, "2023-01-02"),
    (2023, 12, 31, "2024-01-01"),
    (2020, 2, 29, "2020-03-01"),
    (2021, 2, 28, "2021-03-01"),
    (2023, 2, 28, "2023-03-01"),
    (2023, 12, 31, "2024-01-01"),
    (2023, 1, 31, "2023-02-01"),
    (2023, 2, 28, "2023-03-01"),
    (2023, 3, 31, "2023-04-01"),
    (2023, 4, 30, "2023-05-01"),
    (2023, 5, 31, "2023-06-01"),
    (2023, 6, 30, "2023-07-01"),
    (2023, 7, 31, "2023-08-01"),
    (2023, 8, 31, "2023-09-01"),
    (2023, 9, 30, "2023-10-01"),
    (2023, 10, 31, "2023-11-01"),
    (2023, 11, 30, "2023-12-01"),
])
def test_next_date(year, month, day, expected):
    result = next_date(year, month, day)
    assert result == expected